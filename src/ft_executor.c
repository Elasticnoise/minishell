#include "../minishell.h"

void	cmd_to_lower(char **cmd)
{
	int	i;
	int	j;

	i = 0;
	while (cmd[i])
	{
		j = 0;
		while (cmd[i][j])
		{
			cmd[i][j] = ft_tolower(cmd[i][j]);
			j++;
		}
		i++;
	}
}
