#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*str;

	str = b;
	i = 0;
	while (len)
	{
		str[i] = c;
		i++;
		len--;
	}
	return (b);
}
